  extend ActiveSupport::Concern
  included do
  end

  module ClassMethods
  end
  
  def errors_for_field(field)
    arr = self.errors[field]
    if arr.empty?
      return ''
    else
      return (self.class.human_attribute_name(field) + arr.join('且')).html_safe
    end
  end
end


